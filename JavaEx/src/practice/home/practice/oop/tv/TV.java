package practice.home.practice.oop.tv;

public class TV {

	private int x;
	private int y;
	private boolean z;
	
	public void Point (int x, int y, boolean z) {
		this.x=x;
		this.y=y;
		this.z=z;
	}

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}

	public boolean isZ() {
		return z;
	}
	
	
}
